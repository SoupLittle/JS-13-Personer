document.getElementById('name').addEventListener('input', function (event) {
  var inputName = this.value.trim().toLowerCase();


  if (event.key === 'Enter') {
    processInput(inputName);
  }
});

document.getElementById('name').addEventListener('keydown', function (event) {
  if (event.key === 'Enter') {
    event.preventDefault(); 
    var inputName = this.value.trim().toLowerCase();
    processInput(inputName);
  }
});

 
function processInput(inputName) {
  var matchingPerson = ansatt.find(person => {
    var fullName = person.name.toLowerCase();
    return fullName.includes(inputName);
})

  var resultContainer = document.getElementById('resultContainer');
  resultContainer.innerHTML = '';

  if (matchingPerson) {


    var resultImage = document.createElement('img')
    resultImage.src = matchingPerson.image
    resultImage.alt = matchingPerson.name
    resultImage.className = 'result-image'

    var resultName = document.createElement('div')
    resultName.textContent = matchingPerson.name
    resultName.className = 'result-name'

    var resultRole = document.createElement('div')
    resultRole.textContent = matchingPerson.role
    resultRole.className = 'result-role'

    // var resultText = document.createElement('div')
    // resultText.textContent = matchingPerson.text
    // resultText.className ='result-text'

    var resultText = document.createElement('div');
    resultText.innerHTML = matchingPerson.text.replace(/\r\n/g, '<br>');
    resultText.className = 'result-text';
    
    

    resultContainer.appendChild(resultImage);
    resultContainer.appendChild(resultName);
    resultContainer.appendChild(resultRole);
    resultContainer.appendChild(resultText)
    
    var elements = document.querySelectorAll("input");
    
    for(var element of elements) {
    element.value = "";
  }} else {
    alert('No match found! Try again with a different name')
  }

  }

